import { NextRequest, NextResponse } from "next/server";
import { getProfileById } from "@/lib/profiles";
import JSZip from "jszip";

export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const profileId = searchParams.get('id');

    if (!profileId) {
      return NextResponse.json({ error: 'Profile ID is required' }, { status: 400 });
    }

    const profile = getProfileById(profileId);
    if (!profile) {
      return NextResponse.json({ error: 'Profile not found' }, { status: 404 });
    }

    // Generate static HTML content
    const htmlContent = generateStaticHTML(profile, profileId);
    
    // Generate CSS content
    const cssContent = generateStaticCSS();

    // Create ZIP file
    const zip = new JSZip();
    zip.file("index.html", htmlContent);
    zip.file("style.css", cssContent);
    
    // Add a README file
    const readmeContent = `# ${profile.name} - Portfolio

This is a static portfolio website generated by GoRoFolio.

## Files:
- index.html - The main portfolio page
- style.css - Styling for the portfolio
- README.md - This file

## Usage:
1. Open index.html in a web browser
2. The portfolio will display all sections including experience, education, and skills
3. All styles are contained in the CSS file for easy customization

Generated on: ${new Date().toISOString()}
`;
    
    zip.file("README.md", readmeContent);

    // Generate ZIP buffer
    const zipBuffer = await zip.generateAsync({ type: "nodebuffer" });

    // Return ZIP file
    return new NextResponse(zipBuffer, {
      headers: {
        'Content-Type': 'application/zip',
        'Content-Disposition': `attachment; filename="portfolio-${profileId}.zip"`,
      },
    });

  } catch (error) {
    console.error('Export error:', error);
    return NextResponse.json({ 
      error: 'Failed to export portfolio', 
      details: error instanceof Error ? error.message : 'Unknown error' 
    }, { status: 500 });
  }
}

function generateStaticHTML(profile: any, profileId: string): string {
  const formatDate = (dateStr: string | undefined) => {
    if (!dateStr) return "Present";
    const [year, month] = dateStr.split('-');
    const monthNames = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
    return `${monthNames[parseInt(month) - 1]} ${year}`;
  };

  return `<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>${profile.name} - Portfolio</title>
    <link rel="stylesheet" href="style.css">
</head>
<body>
    <div class="container">
        <!-- Header -->
        <header class="header">
            <div class="profile-photo">
                <span class="initials">${profile.name.split(' ').map((n: string) => n[0]).join('')}</span>
            </div>
            <h1 class="name">${profile.name}</h1>
            <h2 class="headline">${profile.headline}</h2>
        </header>

        <!-- External Links -->
        ${profile.links && profile.links.length > 0 ? `
        <section class="links-section">
            <h3>External Links</h3>
            <div class="links-container">
                ${profile.links.map((link: any) => `
                    <a href="${link.url}" target="_blank" rel="noopener noreferrer" class="link-item">
                        ${link.label}
                    </a>
                `).join('')}
            </div>
        </section>
        ` : ''}

        <!-- Summary -->
        <section class="section">
            <h2>Summary</h2>
            <p class="summary">${profile.summary}</p>
        </section>

        <!-- Experience -->
        <section class="section">
            <h2>Experience</h2>
            ${profile.experiences.map((exp: any) => `
                <div class="experience-item">
                    <div class="experience-header">
                        <div>
                            <h3 class="role">${exp.role}</h3>
                            <h4 class="company">${exp.company}</h4>
                        </div>
                        <div class="date">${formatDate(exp.start)} - ${formatDate(exp.end)}</div>
                    </div>
                    <ul class="bullets">
                        ${exp.bullets.map((bullet: string) => `<li>${bullet}</li>`).join('')}
                    </ul>
                </div>
            `).join('')}
        </section>

        <!-- Education -->
        <section class="section">
            <h2>Education</h2>
            ${profile.education.map((edu: any) => `
                <div class="education-item">
                    <div class="education-header">
                        <div>
                            <h3 class="degree">${edu.degree}</h3>
                            <h4 class="school">${edu.school}</h4>
                        </div>
                        <div class="year">${edu.year}</div>
                    </div>
                </div>
            `).join('')}
        </section>

        <!-- Skills -->
        <section class="section">
            <h2>Skills</h2>
            <div class="skills-container">
                ${profile.skills.map((skill: string) => `<span class="skill-tag">${skill}</span>`).join('')}
            </div>
        </section>
    </div>
</body>
</html>`;
}

